'''
- 연산자
    특정한 작업을 수행하기 위해서 사용하는 기호를 의미
    1. 산술 연산자
        +, -, *, **, /, //, %
        숫자 계산을 할 때 사용하는 연산자
'''
a = 7
b = 2

#덧샘
print('{} + {} = {}'.format(a, b, a + b))

#뻴셈
print('{} - {} = {}'.format(a, b, a - b))

#곱셈
print('{} * {} = {}'.format(a, b, a * b))

#거듭곱셈
print('{} ** {} = {}'.format(a, b, a ** b))

#나누기
print('{} / {} = {}'.format(a, b, a/b))

#몫
print('{} // {} = {}'.format(a, b, a // b))

#나머지
print('{} % {} = {}'.format(a, b, a % b))
